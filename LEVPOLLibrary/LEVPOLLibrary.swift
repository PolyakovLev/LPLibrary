import Foundation
import LDAUsefulTools

public final class LPLibrary {
    let name = "LPLibrary"
    
    public func sum(a: Int, b: Int) -> Int {
        return a + b
    }
    
    public func sub(a: Int, b: Int) -> Int {
        return a - b
    }
    
    public init() {}
}
